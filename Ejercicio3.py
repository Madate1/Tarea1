
#Ejercicio 3: Crear un script que encuentre el elemento menor de una lista

lista = [23, 44, -12, 65,-54, 99, -32]
menor = "init"
for x in lista:
    if menor == "init" or menor > "init":
        menor = x
        menor > x
    else:
        if x < menor:
            menor = x
      
print(f"el numero menor es {menor}")
